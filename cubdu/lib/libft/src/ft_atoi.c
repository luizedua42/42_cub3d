#include "libft.h"

int	ft_atoi(const char *s)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	b = 1;
	c = 0;
	while ((s[a] == '\t' || s[a] == '\f' || s[a] == '\n' \
			|| s[a] == '\v' || s[a] == '\r') || s[a] == ' ')
		a++;
	if (s[a] == '+' || s[a] == '-')
	{
		if (s[a] == '-')
		{
			b = -1;
		}
		a++;
	}
	while (s[a] >= '0' && s[a] <= '9')
	{
		c = (s[a] - '0') + (c * 10);
		a++;
	}
	return (c * b);
}
